# IN = Contido Em
# NOT IN = Não Está Contido Em

# x in [12, 13, 14] ## Retorna True se estiver contido na lista.
# x in (12, 13, 14) ## Retorna True se estiver contido na lista.
# x in {12, 13, 14} ## Retorna True se estiver contido na lista.
# not in ## Retorna o oposto da anterior.

